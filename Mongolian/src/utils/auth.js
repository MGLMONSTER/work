import Cookies from 'js-cookie'

const TokenKey = 'Admin-Token'
const AdminAdmin = "AdminAdmin"
const AdminAdminAdmin = "AdminAdmin-Admin"
const PassWord = "Admin-admin"
const UserName = "Admin-UserName"
const SetState = "Admin-setState"
export function getToken() {
    return Cookies.get(TokenKey)
}

export function setToken(token) {
    return Cookies.set(TokenKey, token)
}

export function removeToken() {
    return Cookies.remove(TokenKey)
}

export function setZhiBId(val) {
    return Cookies.set(AdminAdmin, val)
}
export function getZhiBId() {
    return Cookies.get(AdminAdmin)
}
export function setInfoSuccess(val) {
    return Cookies.set(AdminAdminAdmin)
}
export function getInfoSuccess() {
    return Cookies.get(AdminAdminAdmin)
}
export function setPassWord(val) {
    return Cookies.set(PassWord,val);
}
export function getPassWord() {
    return Cookies.get(PassWord)
}
export function setUserName(val) {
    return Cookies.set(UserName,val);
}
export function getUserName() {
    return Cookies.get(UserName)
}
export function setState(val) {
    return Cookies.set(SetState,val);
}
export function getState() {
    return Cookies.get(SetState)
}
export function removePassword() {    //清除保存的密码
    return Cookies.remove(PassWord)
}